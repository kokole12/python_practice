#!/usr/bin/python3
def add(a, b):
    """
        My addition function

        args:
            a: first argument
            b: second argument
        
        Returns:
            The value of a + b
        
        return (a + b)
    """
    return (a + b)

def sub(a, b):
    """
        My subtraction function

        Args:
            a: first argument
            b: second argument
        
        Returns:
            The value of a - b
        
        return (a - b)
    """
    return (a - b)

def div(a, b):
    """
        My division function

        Args:
            a: first arg
            b:  second arg
        
        Return:
            The value of a / b

        return a / b
    """
    return (a /b )

def mul(a, b):
    """
        My multiplication function

        Args:
            a: first arg
            b: second arg
        Returns:
            the value of a * b
        return a *  b
    """
    return (a * b)
